public class Escape_Sequence {
    public static void main(String[] args) {
        int a,b;
        System.out.println("Different way of using backslash");
        System.out.println("1 the use of backslash n along with the message");
        System.out.println("Name: Akash Mittal \n Age: 16yrs \n Class X");
        a = 14;
        b = 16;
        System.out.println("2 The use of backslash n separated by +");
        System.out.println("First number = " + a + "\n" + "second number = " + b + "\n" + "Sum = " + (a+b));
        System.out.println('M');
    }
}
